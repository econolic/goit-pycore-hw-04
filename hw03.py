import sys
from pathlib import Path
from colorama import init, Fore, Style

# –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è colorama –∑ –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω–∏–º —Å–∫–∏–¥–∞–Ω–Ω—è–º –∫–æ–ª—å–æ—Ä—ñ–≤
init(autoreset=True)

def visualize_directory(path, indent=0):
    """
    –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –≤–∏–≤–æ–¥–∏—Ç—å —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∑ –∫–æ–ª—å–æ—Ä–æ–≤–∏–º –º–∞—Ä–∫—É–≤–∞–Ω–Ω—è–º.

    –ü–∞—Ä–∞–º–µ—Ç—Ä–∏:
    path (Path): –®–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∞–±–æ —Ñ–∞–π–ª—É.
    indent (int): –†—ñ–≤–µ–Ω—å –≤—ñ–¥—Å—Ç—É–ø—É –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –≤–∫–ª–∞–¥–µ–Ω–æ—Å—Ç—ñ.
    """
    if path.is_dir():
        # –í–∏–≤–æ–¥–∏–º–æ —ñ–º'—è –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó —Å–∏–Ω—ñ–º –∫–æ–ª—å–æ—Ä–æ–º
        print(Fore.BLUE + Style.BRIGHT + '  ' * indent + f"üìÇ {path.name}")
        # –û–±—Ö–æ–¥–∏–º–æ –≤—Å—ñ –µ–ª–µ–º–µ–Ω—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
        for item in path.iterdir():
            visualize_directory(item, indent + 1)
    elif path.is_file():
        # –í–∏–≤–æ–¥–∏–º–æ —ñ–º'—è —Ñ–∞–π–ª—É –∑–µ–ª–µ–Ω–∏–º –∫–æ–ª—å–æ—Ä–æ–º
        print(Fore.GREEN + '  ' * indent + f"üìú {path.name}")

def main():
    """
    –ì–æ–ª–æ–≤–Ω–∞ —Ñ—É–Ω–∫—Ü—ñ—è: –æ—Ç—Ä–∏–º—É—î —à–ª—è—Ö –∑ –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ —Ä—è–¥–∫–∞ —Ç–∞ –∑–∞–ø—É—Å–∫–∞—î –≤—ñ–∑—É–∞–ª—ñ–∑–∞—Ü—ñ—é.
    """
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –∫—ñ–ª—å–∫–æ—Å—Ç—ñ –∞—Ä–≥—É–º–µ–Ω—Ç—ñ–≤
    if len(sys.argv) != 2:
        print("–í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è: python hw03.py /—à–ª—è—Ö/–¥–æ/–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó")
        sys.exit(1)

    # –ü–µ—Ä–µ—Ç–≤–æ—Ä–µ–Ω–Ω—è –∞—Ä–≥—É–º–µ–Ω—Ç—É –≤ –æ–±'—î–∫—Ç Path
    dir_path = Path(sys.argv[1])

    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —ñ—Å–Ω—É–≤–∞–Ω–Ω—è —à–ª—è—Ö—É —Ç–∞ —á–∏ —î –≤—ñ–Ω –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é
    if not dir_path.exists():
        print(f"–ü–æ–º–∏–ª–∫–∞: –®–ª—è—Ö '{dir_path}' –Ω–µ —ñ—Å–Ω—É—î.")
        sys.exit(1)
    if not dir_path.is_dir():
        print(f"–ü–æ–º–∏–ª–∫–∞: '{dir_path}' –Ω–µ —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é.")
        sys.exit(1)

    # –í–∏–≤–µ–¥–µ–Ω–Ω—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
    print(f"–°—Ç—Ä—É–∫—Ç—É—Ä–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó '{dir_path}':")
    visualize_directory(dir_path)

if __name__ == "__main__":
    main()